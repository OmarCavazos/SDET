// Equal
console.log(5 == 5);
console.log(5 == 6);
console.log("test" == "test");
console.log("test" == "Test"); // Case sensitive!
console.log(5 == "5");

// Not Equal
console.log(5 != 5);
console.log(5 != 6);
console.log("test" != "test");
console.log("test" != "Test");

// Strict Equal
console.log(5 === 5);
console.log(5 === 6);
console.log("test" === "test");
console.log("test" === "Test");
console.log(5 === "5");

// Strict Not Equal
console.log(5 !== 5);
console.log(5 !== 6);
console.log("test" !== "test");
console.log("test" !== "Test");
console.log(5 !== "5");

// Greater Than
console.log(5 > 6);
console.log(6 > 5);
console.log(5 > "4");
console.log(5 > 5);

// Less Than
console.log(5 < 6);
console.log(6 < 5);
console.log(5 < "6");
console.log(5 < "4");
console.log(5 < 5);

// Greater or Equal Than
console.log(5 >= 6);
console.log(6 >= 5);
console.log(5 >= "6");
console.log(5 >= 5);

// Less or Equal Than
console.log(5 <= 6);
console.log(6 <= 5);
console.log(5 <= "6");
console.log(5 <= 5);
